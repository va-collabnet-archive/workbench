package org.ihtsdo.tk.api;

public class ContraditionException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ContraditionException() {
		super();
	}

	public ContraditionException(String message) {
		super(message);
	}

	public ContraditionException(Throwable cause) {
		super(cause);
	}

	public ContraditionException(String message, Throwable cause) {
		super(message, cause);
	}

}
