    private void actionAddAnotherTask(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actionAddAnotherTask
        // :!!!: main:: actionAddAnotherTask, refresh
        BatchActionEditTask baet = new BatchActionEditTask();
        baet.setEnabled(true);
        baet.setVisible(true);
        baet.invalidate();
        batchActionTaskList.add(baet);
        GridLayout gl = new GridLayout(batchActionTaskList.size(), 1, 0, 0);
        batchActionTaskViewPanel.setLayout(gl);

        batchActionTaskViewPanel.removeAll();
        for (BatchActionEditTask b : batchActionTaskList) {
            batchActionTaskViewPanel.add(b);
        }
        jScrollBatchActionTasks.setViewportView(batchActionTaskViewPanel);
        jScrollBatchActionTasks.invalidate();
        jScrollBatchActionTasks.validate();
        jScrollBatchActionTasks.doLayout();
        this.invalidate();
        this.validate();
        System.out.println(":!!: main:: actionAdd jScrollBatchActionTask");
    }//GEN-LAST:event_actionAddAnotherTask

