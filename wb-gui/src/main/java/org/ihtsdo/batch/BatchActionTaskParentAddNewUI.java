    @Override // I_BatchActionTask
    public void updateExisting(List<RelationshipVersionBI> existingParents, List<ComponentVersionBI> existingRefsets, List<RelationshipVersionBI> existingRoles, List<ComponentVersionBI> parentLinkages) {
        DefaultComboBoxModel dcbm = (DefaultComboBoxModel) jComboBoxLinkage.getModel();
        ComponentVersionBI selectedItem = (ComponentVersionBI) dcbm.getSelectedItem();

        dcbm.removeAllElements();
        for (ComponentVersionBI componentVersionBI : parentLinkages) {
            dcbm.addElement(componentVersionBI);
        }

        if (dcbm.getSize() == 0) {
            // empty list
        } else if (selectedItem == null) {
            // no prior selection
            dcbm.setSelectedItem(dcbm.getElementAt(0));
        } else {
            // Search by nid
            int selectedIdx = -1;
            for (int i = 0; i < dcbm.getSize(); i++) {
                ComponentVersionBI cvbi = (ComponentVersionBI) dcbm.getElementAt(i);
                if (cvbi.getNid() == selectedItem.getNid()) {
                    selectedIdx = i;
                    selectedItem = cvbi;
                    break;
                }
            }

            if (selectedIdx >= 0) {
                // prior selection exists in new list
                dcbm.setSelectedItem(selectedItem);
                jComboBoxLinkage.setSelectedIndex(selectedIdx);
            } else {
                // prior selection does not exist in new list
                dcbm.setSelectedItem(dcbm.getElementAt(0));
                jComboBoxLinkage.setSelectedIndex(0);
            }
        }
    }

    @Override // I_BatchActionTask
    public BatchActionTask getTask(EditCoordinate ec, ViewCoordinate vc, List<ConceptChronicleBI> concepts) throws IOException {

        DefaultComboBoxModel dcbm = (DefaultComboBoxModel) jComboBoxLinkage.getModel();
        ConceptVersionBI fromParentLinkageBI = (ConceptVersionBI) dcbm.getSelectedItem();
        UUID uuidParentLinkage = fromParentLinkageBI.getPrimUuid(); // 'Is a', 'is a'

        I_AmTermComponent termNewParentDest = ((ValueDndNidUI) jPanelDndNewParent).getTermComponent();
        if (termNewParentDest != null && termNewParentDest.getUUIDs().size() > 0) {
            UUID uuidNewParentDest = termNewParentDest.getUUIDs().get(0);
            ((BatchActionTaskParentAddNew) task).setSelectedRoleTypeUuid(uuidParentLinkage);
            ((BatchActionTaskParentAddNew) task).setSelectedDestUuid(uuidNewParentDest);
            return task;
        } else {
            BatchActionEventReporter.add(new BatchActionEvent(null, BatchActionTaskType.PARENT_ADD_NEW,
                    BatchActionEventType.TASK_INVALID, "value not set"));
            return null;
        }
    }
